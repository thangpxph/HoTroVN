package com.example.hotrovn.model;

public class Spinner {
    private int imgSpinner;
    private String tvSpinner;

    public Spinner(int imgSpinner, String tvSpinner) {
        this.imgSpinner = imgSpinner;
        this.tvSpinner = tvSpinner;
    }

    public int getImgSpinner() {
        return imgSpinner;
    }

    public void setImgSpinner(int imgSpinner) {
        this.imgSpinner = imgSpinner;
    }

    public String getTvSpinner() {
        return tvSpinner;
    }

    public void setTvSpinner(String tvSpinner) {
        this.tvSpinner = tvSpinner;
    }
}

